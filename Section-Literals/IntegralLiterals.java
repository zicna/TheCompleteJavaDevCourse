public class IntegralLiterals{
    public static void main(String[] args) {
        // * Decimal literals

        int decimalInt = 12;

        // *Octal literals

        int octalInt = 014;

        // *Hexadecimal literals

        int hexaInt = 0xc;

        // *Binary literals

        int binInt = 0b1100;

        System.out.println("integer used decimal literal " + decimalInt);
        System.out.println("integer used octal literal " + octalInt);
        System.out.println("integer used hexadecimal literal " + hexaInt);
        System.out.println("integer used binary literal " + binInt);
    }
}